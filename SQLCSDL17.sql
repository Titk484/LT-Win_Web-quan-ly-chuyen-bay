CREATE TABLE KHACHHANG 
(  
	MAKH		NVARCHAR(12)	CONSTRAINT KH_MAKH_PK	PRIMARY KEY,
	TEN			NVARCHAR(25)	CONSTRAINT KH_TENKH_NN	NOT NULL,
	GIOITINH	NVARCHAR(4)		CONSTRAINT KH_GT_NN		CHECK (GIOITINH IN('Nam','Nu')),
	NGAYSINH	DATE			CONSTRAINT KH_NS_NN		NOT NULL,
	CMND		INT				CONSTRAINT KH_CMND_NN	NOT NULL,
	SDT			INT				CONSTRAINT KH_SDT_NN	NOT NULL, 
	DIACHI		NVARCHAR(50)	CONSTRAINT KH_DC_NN		NOT NULL
) 
GO
CREATE TABLE NHANVIEN 
(  
	MANV		NVARCHAR(10)		CONSTRAINT NV_MANV_NN	UNIQUE,	 
	TEN			NVARCHAR(25)	CONSTRAINT NV_TEN_U		NOT NULL,
	GIOITINH	NVARCHAR(4)		CONSTRAINT NV_GT_NN		CHECK (GIOITINH IN('Nam','Nu')),
	CMND		INT				CONSTRAINT NV_CMND_NN	NOT NULL,
	SDT			INT				CONSTRAINT NV_SDT_NN	NOT NULL,
	CHUCVU		NVARCHAR(25)	CONSTRAINT NV_CV_NN		NOT NULL
	PRIMARY KEY (MANV)
)
GO
CREATE TABLE MAYBAY
(  
	MAMB	NVARCHAR(3)		CONSTRAINT MB_MAMB		NOT NULL,
	SUMSC	INT				CONSTRAINT MB_SMC	NOT NULL,
	SUMSG	INT				CONSTRAINT MB_SMG	NOT NULL,
	SUMSGTG	INT				CONSTRAINT MB_SMGTG	NOT NULL,
	SUMSGPT	INT				CONSTRAINT MB_SMGPT	NOT NULL
	PRIMARY KEY (MAMB) 
)
GO
CREATE TABLE CHUYENBAY 
(  
	MAMB		NVARCHAR(3)		CONSTRAINT CB_MAMB_PK			NOT NULL,
	MACB		NVARCHAR(10)	CONSTRAINT CB_MACB_NN		NOT NULL,
	MANV		NVARCHAR(10)	CONSTRAINT CB_MANV_NN			FOREIGN KEY (MANV) REFERENCES NHANVIEN (MANV),
	NGAYDI		DATE			CONSTRAINT CB_NDI_NN			NOT NULL,
	NGAYDEN		DATE			CONSTRAINT CB_NDEN_NN			NOT NULL, 
	SBDI		NVARCHAR(15)	CONSTRAINT CB_SBDI_NN			NOT NULL, 
	SBDEN		NVARCHAR(15)	CONSTRAINT CB_SBDEN_NN			NOT NULL, 
	GHE			INT				CONSTRAINT CB_GHE_NN			NOT NULL,
	GIATG		FLOAT			CONSTRAINT CB_GIATG_NN			NOT NULL,
	GIAPT		FLOAT			CONSTRAINT CB_GIAPT_NN			NOT NULL
	
) 
GO


CREATE TABLE PHANCONG 
(  
	MACB	NVARCHAR(10)		CONSTRAINT PC_MACB_PK	PRIMARY KEY (MACB),
	MANV	NVARCHAR(10)		CONSTRAINT PC_MANV_FK	FOREIGN KEY (MANV) REFERENCES NHANVIEN (MANV),
	SBDI	NVARCHAR(15)	CONSTRAINT PC_SBDI_NN	NOT NULL, 
	SBDEN	NVARCHAR(15)	CONSTRAINT PC_SBDI_NN   NOT NULL, 
	NGAYDI	DATE			CONSTRAINT PC_NDI_NN	NOT NULL,
	NGAYDEN	DATE			CONSTRAINT PC_NDEN_NN   NOT NULL
)

GO
CREATE TABLE DVE 
(  
	MACB		NVARCHAR(10)		CONSTRAINT VE_MACB_NN   FOREIGN KEY (MACB) REFERENCES PHANCONG	(MACB),
	MAVE		NVARCHAR(8)		CONSTRAINT VE_MAVE_U	UNIQUE, 
	MAKH		NVARCHAR(12)	CONSTRAINT VE_MAKH		FOREIGN KEY (MAKH) REFERENCES KHACHHANG (MAKH),
	NGAYDI		DATE			CONSTRAINT VE_NDI_NN	NOT NULL,
	NGAYDEN		DATE			CONSTRAINT VE_NDEN_NN   NOT NULL,
	SBDI		NVARCHAR(15)	CONSTRAINT VE_SBDI_NN   NOT NULL, 
	SBDEN		NVARCHAR(15)	CONSTRAINT VE_SBDI_NN   NOT NULL,
	GHE			NVARCHAR(5)		CONSTRAINT VE_GHE_NN	NOT NULL,
	GIAVE		FLOAT			CONSTRAINT VE_GIAVE_NN	NOT NULL
